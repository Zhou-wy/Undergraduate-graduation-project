/*******************************************************************************
* 函 数 名         : USART1_IRQHandler
* 函数功能		   : USART1中断函数
* 输    入         : 无
* 输    出         : 无
*******************************************************************************/ 	
uint8_t r=0;
void USART1_IRQHandler(void)                	//串口1中断服务程序
{
	if(USART_GetITStatus(USART1, USART_IT_RXNE) != RESET)  //接收中断
	{
			UART_RX_BUF[r]=USART_ReceiveData(USART1);
			r++;
			if(r==2 && UART_RX_BUF[0] == FRAME_HEADER && UART_RX_BUF[1] == FRAME_HEADER)
			{
					//LED2=1;		
			}
		
			if(r>2 && (r==UART_RX_BUF[2]+2))
			{
				Recv_flag=1 ;
				r=0;
			}

	} 


}


